package Ex03_Operators;

public class Lab_013_Relational_Operators {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("a == b : " + (a == b));   // false
        System.out.println("a != b : " + (a != b));   // true
        System.out.println("a > b  : " + (a > b));    // false
        System.out.println("a < b  : " + (a < b));    // true
        System.out.println("a >= b : " + (a >= b));   // false
        System.out.println("a <= b : " + (a <= b));   // true


    }
}

